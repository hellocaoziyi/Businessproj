ORG 0000H
	LJMP MAIN

ORG 0100H
MAIN:
	CLR A
	MOV A,#01H
	MOV 30H,A ;SECONDS
	MOV 31H,A ;MINUTES
	MOV 32H,A ;HOURS

LOOP:
	CALL KEYSCAN
	CALL DELAY
	INC 30H
	JMP LOOP

KEYSCAN:
	JB P2.0,NOKEYPRESS
	JB P2.1,NOKEYPRESS
	MOV 30H,#00H
	MOV 31H,#00H
	MOV 32H,#00H
	RET
NOKEYPRESS:
	RET

DELAY:
	MOV R6,#200
DELAY1:
	NOP
	DJNZ R6,DELAY1
	RET
	
